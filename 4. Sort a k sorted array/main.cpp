void solve(){
    vector<int> arr = {6, 5, 3, 2, 8, 10, 9};
    int k = 3;
    int n = arr.size();

    priority_queue<int, vector<int>, greater<int>> minHeap;
    vector<int> ans;

    for (int i = 0; i < n; i++) {
        minHeap.push(arr[i]);

        // If heap size exceeds k, pop the top and add to result
        if (minHeap.size() > k) {
            ans.push_back(minHeap.top());
            minHeap.pop();
        }
    }

    // Pop remaining elements
    while (!minHeap.empty()) {
        ans.push_back(minHeap.top());
        minHeap.pop();
    }

    cout << ans << "\n";  // output the sorted array
}


