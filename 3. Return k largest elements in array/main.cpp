void solve(){
    int n, k;
    cin >> n >> k;
    vi arr(n);
    cin >> arr;

    // Min-heap to store k largest elements
    priority_queue<int, vector<int>, greater<int>> minHeap;

    // Maintain a heap of k largest elements
    for(int i = 0; i < n; i++) {
        if(minHeap.size() < k) {
            minHeap.push(arr[i]);
        } else if(arr[i] > minHeap.top()) {
            minHeap.pop();
            minHeap.push(arr[i]);
        }
    }

    // Extract and store result from heap
    vi result;
    while(!minHeap.empty()) {
        result.push_back(minHeap.top());
        minHeap.pop();
    }

    // Optional: sort in descending order
    sort(result.rbegin(), result.rend());

    cout << result << "\n";
}

